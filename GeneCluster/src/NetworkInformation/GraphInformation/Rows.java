package NetworkInformation.GraphInformation;

public class Rows {
	public String adjacencies;
	public String data;
	public String id;
	public String name;

	public String getAdjacencies() {
		return adjacencies;
	}

	public void setAdjacencies(String adjacencies) {
		this.adjacencies = adjacencies;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}

